<!--history-->
        <div class=" s-heading mt-5">HISTORY</div>
        <p class=" mx-4 py-3 text-m"> After independence, the University Grants Commission, headed by S. Radhakrishnan, recommended the introduction of voluntary national service in academic institutions. After proper consideration and examination of the idea by the Central Advisory Board of Education (CABE) and guideline by our first PM Pt. Jawaharlal Nehru, In May 1969, a conference of student representatives (of universities and institutions of higher education) convened by the Ministry of Education and the University Grants Commission also unanimously agreed that a national-service scheme could be an instrument for national integration and orientation camp was organized at Rajghat. This camp was concluded on 7 June 1969. KK Gupta from DU was declared the first volunteer. On 24 September 1969, the then Union Education Minister V.K.R.V. Rao launched the NSS at 37 universities all states and since then the number of students strength increased from 40,000 to over 3.8 million up to the end of March 2018 students in various universities, colleges and institutions of higher learning have volunteered to take part in various community service programs.</p>
